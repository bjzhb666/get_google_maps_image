func GetRegionsCenter() []Region {
	var regions []Region

	for name, array := range regionCenter {
		regions = append(regions, Region{
			Name:        name,
			RadiusX:     1,
			RadiusY:     1,
			CenterGPS:   common.Point{array[1], array[0]},
			CenterWorld: googlemaps.LonLatToMeters(common.Point{array[1], array[0]}),
		})
	}

	return regions
}
